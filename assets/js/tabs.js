function showSubjectTabs() {
    const x = document.getElementById("exam-div");
    if (x.style.display === "none") {
        x.style.display = "block";

    } else {
        x.style.display = "none";
    }
}

function showStudentsTabs() {
    const x = document.getElementById("student-div");
    if (x.style.display === "none") {
        x.style.display = "block";

    } else {
        x.style.display = "none";
    }
}